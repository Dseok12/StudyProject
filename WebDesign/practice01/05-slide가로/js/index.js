var slideCount = $('.slideImg').length;
var currentIndex = 0;
var slidePosition;

setInterval(function(){
    slidePosition = currentIndex * (-1200) + 'px';
    if(currentIndex < slideCount - 1){
        currentIndex++;
    }else{
        currentIndex = 0;
    }
    $('.slideList').animate({left:slidePosition}, 400)

}, 3000);












































































































































